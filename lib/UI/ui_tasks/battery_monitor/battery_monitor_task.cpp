#include <Arduino.h>

#include "../../../../include/battery_level.h"
#include "battery_monitor.hpp"
#include "vars.h"

void battery_monitor_task(void *pv) {
  while (true) {
    set_var_battery_level(((String)"Battery: " + read_battery_level()).c_str());
    delay(TASK_POLLING_RATE);
  }
}